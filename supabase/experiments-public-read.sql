CREATE TABLE IF NOT EXISTS experiments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title TEXT NOT NULL,
  goal TEXT NOT NULL,
  description TEXT NOT NULL,
  video_url TEXT NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

ALTER TABLE experiments ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "experiments_select_public" ON experiments;
DROP POLICY IF EXISTS "experiments_insert_public" ON experiments;
DROP POLICY IF EXISTS "experiments_update_public" ON experiments;
DROP POLICY IF EXISTS "experiments_delete_public" ON experiments;

CREATE POLICY "experiments_select_public"
ON experiments FOR SELECT
TO public
USING (true);

CREATE POLICY "experiments_insert_public"
ON experiments FOR INSERT
TO public
WITH CHECK (true);

CREATE POLICY "experiments_update_public"
ON experiments FOR UPDATE
TO public
USING (true)
WITH CHECK (true);

CREATE POLICY "experiments_delete_public"
ON experiments FOR DELETE
TO public
USING (true);
